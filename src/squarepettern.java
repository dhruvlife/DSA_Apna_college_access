public class squarepettern {
    public static void main(String[]args){
        System.out.println(" .  .  .  -  -  -  .  .  .");
        System.out.println("square pettern");
        System.out.println(" .  .  .  -  -  -  .  .  .");
        System.out.println("");
        int n=4;
        int m=6;
        for (int i=1;i<=n;i++){
            for (int j=1;j<=m;j++){
                System.out.print(" * ");
            }
            System.out.println("");
        }
    }
}
